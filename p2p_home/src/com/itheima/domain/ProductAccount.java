package com.itheima.domain;

import java.io.Serializable;
import java.util.Date;

//投资记录
public class ProductAccount implements Serializable{

    /**
     *
     */
    private static final long serialVersionUID = 1L;
    private int id;
    private String pa_num;
    private Date pa_date;
    private Customer c;
    private Product p;
    private double money; // 投资金额
    private double interest; // 本次预计收益
    private int status; // 0 未到期 1到期

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPa_num() {
        return pa_num;
    }

    public void setPa_num(String pa_num) {
        this.pa_num = pa_num;
    }

    public Date getPa_date() {
        return pa_date;
    }

    public void setPa_date(Date pa_date) {
        this.pa_date = pa_date;
    }

    public Customer getC() {
        return c;
    }

    public void setC(Customer c) {
        this.c = c;
    }

    public Product getP() {
        return p;
    }

    public void setP(Product p) {
        this.p = p;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }

    public double getInterest() {
        return interest;
    }

    public void setInterest(double interest) {
        this.interest = interest;
    }

}

