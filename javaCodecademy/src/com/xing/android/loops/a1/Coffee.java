package com.xing.android.loops.a1;

public class Coffee {

    public static void main(String[] args) {

        // initialize cupsOfCoffee
        int cupsOfCoffee = 1;

        // add while loop with counter
        while (cupsOfCoffee <= 100) {

            System.out.println("Fry drinks cup of coffee #" + cupsOfCoffee);
            cupsOfCoffee++;
        }
    }
}
